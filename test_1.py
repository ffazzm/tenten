def filter_warna(pixel):
    r = pixel[0]
    g = pixel[1]
    b = pixel[2]
    mean_px = (r + g + b) / 3
    if abs(max(pixel) - mean_px) < 20:
        return 'remove'
    else:
        return 'keep'

